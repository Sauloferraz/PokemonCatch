#include "Random.h"

// ---------------------------------------------------------------------------------

random_device Random::seed;
mt19937       Random::mt { seed() };

// ---------------------------------------------------------------------------------

IntRand::IntRand(int min, int max): dist(min, max) {}

// ---------------------------------------------------------------------------------

int IntRand::Rand()
{ return dist(mt); }

// ---------------------------------------------------------------------------------

FloatRand::FloatRand(float min, float max) : dist(min, max) {}

// ---------------------------------------------------------------------------------

float FloatRand::Rand()
{ return dist(mt); }

